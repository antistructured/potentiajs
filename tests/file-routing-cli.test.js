describe('file routing CLI generate command', () => {
  test('generates routes with default options relative to cwd', async () => {
    await setupRoutes('default-app');
    const cwd = resolve(fixtureRoot, 'default-app');
    const io = captureIo(cwd);

    try {
      const code = await main(['routes', 'generate'], io);

      expect(code).toBe(0);
      expect(io.stdout.text).toContain('Generated file routes:');
      expect(io.stdout.text).toContain('- root: routes');
      expect(io.stdout.text).toContain('- output: .potentia/routes.generated.js');
      expect(io.stdout.text).toContain('- routes: 2');
      expect(io.stdout.text).toContain('- scopes: 0');
      expect(io.stderr.text).toBe('');

      const generated = await readFile(resolve(cwd, '.potentia/routes.generated.js'), 'utf8');
      expect(generated).toStartWith('// Generated by PotentiaJS. Do not edit by hand.');
      expect(generated).toContain("import { createRoutes, mount } from '@potentiajs/core';");
      expect(generated).not.toContain('node:fs');
    } finally {
      await cleanup();
    }
  });

  test('supports --cwd, --root, --out, and --package override', async () => {
    await setupRoutes('custom-app', 'app-routes');
    const cwd = resolve(fixtureRoot, 'custom-app');
    const io = captureIo('/tmp/ignored-cwd');

    try {
      const code = await main([
        'routes',
        'generate',
        '--cwd',
        cwd,
        '--root',
        'app-routes',
        '--out',
        'generated/routes.js',
        '--package',
        '@example/core'
      ], io);

      expect(code).toBe(0);
      expect(io.stdout.text).toContain('- root: app-routes');
      expect(io.stdout.text).toContain('- output: generated/routes.js');

      const generated = await readFile(resolve(cwd, 'generated/routes.js'), 'utf8');
      expect(generated).toContain("import { createRoutes, mount } from '@example/core';");
      expect(generated).not.toContain("import { createRoutes, mount } from '@potentiajs/core';");
    } finally {
      await cleanup();
    }
  });

  test('returns code 1 and safe diagnostics for generation failure', async () => {
    await cleanup();
    await mkdir(resolve(fixtureRoot, 'failure-app'), { recursive: true });
    const cwd = resolve(fixtureRoot, 'failure-app');
    const io = captureIo(cwd);

    try {
      const code = await main(['routes', 'generate'], io);

      expect(code).toBe(1);
      expect(io.stdout.text).toBe('');
      expect(io.stderr.text).toContain('File route generation failed:');
      expect(io.stderr.text).toContain('POTENTIA_FILE_ROUTE_MISSING_ROOT');
      expect(io.stderr.text).not.toContain(' at ');
    } finally {
      await cleanup();
    }
  });

  test('returns code 2 and usage for invalid usage', async () => {
    const io = captureIo(fixtureRoot);

    const code = await main(['routes', 'generate', '--watch'], io);

    expect(code).toBe(2);
    expect(io.stdout.text).toBe('');
    expect(io.stderr.text).toContain(USAGE);
    expect(io.stderr.text).toContain('Usage error: unknown option: --watch');
  });
});

