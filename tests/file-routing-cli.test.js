async function setupRoutes(name, routesDir = 'routes') {
  await cleanup();
  const root = resolve(fixtureRoot, name, routesDir);
  await mkdir(resolve(root, 'users'), { recursive: true });
  await writeFile(resolve(root, 'index.js'), [
    "import { ok, route, text } from '@potentiajs/core';",
    "export default route('GET', '/', () => ok(text('home')));",
    ''
  ].join('\n'));
  await writeFile(resolve(root, 'users/[id].js'), [
    "import { json, ok, route } from '@potentiajs/core';",
    "export default route('GET', '/:id', (ctx) => ok(json({ id: ctx.params.id })));",
    ''
  ].join('\n'));
}

async function cleanup() {
  await rm(fixtureRoot, { recursive: true, force: true });
}

function captureIo(cwd) {
  return {
    cwd,
    stdout: captureStream(),
    stderr: captureStream()
  };
}

function captureStream() {
  return {
    text: '',
    write(chunk) {
      this.text += String(chunk);
    }
  };
}

